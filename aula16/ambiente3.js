let v = function(x){
    return x*2

}

console.log(v(5))

//Nesse caso, o numero cinco é um parametro real destinado à variavel v, que recebe a função x, que calcula o dobro do valor recebido por v, então, o dobro de v=5 seria 10.
//E possivel usarmos uma função dentro de uma variavel, mas é um pouco mais avançado.
